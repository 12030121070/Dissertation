package com.sj.timetable;

public class TimetableVariables {
	
	String pname;
	int pid;
	String bname;
	int bid;
	String div;
	int did;
	
	public String getPname(){
		return pname;
	}
	public void setPname(String pname){
		this.pname=pname;
	}
	
	public int getPid(){
		return pid;
	}
	public void setPid(int pid){
		this.pid=pid;
	}
	
	public String getBname(){
		return bname;
	}
	public void setBname(String bname){
		this.bname=bname;
	}
	
	public int getBid(){
		return bid;
	}
	public void setBid(int bid){
		this.bid=bid;
	}
	
	public String getDname(){
		return div;
	}
	public void setDname(String div){
		this.div=div;
	}
	
	public int getDid(){
		return did;
	}
	public void setDid(int did){
		this.did=did;
	}
	

}
